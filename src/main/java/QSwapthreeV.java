public class QSwapthreeV {
    //Swap two variables, using a third variable
    public static void swapthreevariables() {
        int a = 100;
        int b = 200;

        System.out.println("Before Swapping " + a + " " + b);//100 200
        int t = a;
        a = b;
        b = t;
        System.out.println("After Swapping " + a + " " + b);//200 100);

    }


}
